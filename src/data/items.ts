// O(1) lookup: id → name
export const idToName = new Map<number, string>(
  items.map((item) => [item.id, item.name])
);

// O(1) lookup: name (case-insensitive) → id
export const nameToId = new Map<string, number>();
for (const item of items) {
  nameToId.set(item.name.toLowerCase(), item.id);
  // Add alternate names for items with "A/B" format (e.g. "Monster Manual/Monster Manuel")
  if (item.name.includes('/')) {
    for (const alias of item.name.split('/').map((s) => s.trim())) {
      if (alias) nameToId.set(alias.toLowerCase(), item.id);
    }
  }
}

export function getIdByName(name: string): number | undefined {
  return nameToId.get(name.toLowerCase());
}

export function getNameById(id: number): string | undefined {
  return idToName.get(id);
}

